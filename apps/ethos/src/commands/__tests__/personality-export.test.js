// Test group 4: Personality export exclusions
// ---------------------------------------------------------------------------
describe('personality export — file exclusions', () => {
    it('personal files should not appear in export archive', () => {
        // These are the files that personality export MUST exclude
        const personalFiles = ['MEMORY.md', 'USER.md', 'kanban.db', 'dream-state.json'];
        // A proper export should only have these kinds of entries
        const exportEntries = [
            { relPath: 'personalities/alice/SOUL.md', content: Buffer.from('# I am Alice') },
            { relPath: 'personalities/alice/config.yaml', content: Buffer.from('name: alice') },
            { relPath: 'personalities/alice/toolset.yaml', content: Buffer.from('- read_file') },
            {
                relPath: 'secrets.manifest.yaml',
                content: Buffer.from('personality: alice\nexported_at: 2026-05-22T10:00:00Z\n'),
            },
        ];
        const tar = buildTar(exportEntries);
        const parsed = parseTar(tar);
        const paths = parsed.map(([p]) => p);
        for (const personal of personalFiles) {
            expect(paths).not.toContain(`personalities/alice/${personal}`);
        }
        // But should include the structural files
        expect(paths).toContain('personalities/alice/SOUL.md');
        expect(paths).toContain('personalities/alice/config.yaml');
        expect(paths).toContain('personalities/alice/toolset.yaml');
        expect(paths).toContain('secrets.manifest.yaml');
    });
    it('MCP token files should not appear in export archive', () => {
        const exportEntries = [
            { relPath: 'personalities/alice/config.yaml', content: Buffer.from('name: alice') },
            {
                relPath: 'personalities/alice/mcp/linear/config.yaml',
                content: Buffer.from('url: https://linear.app'),
            },
        ];
        const tar = buildTar(exportEntries);
        const parsed = parseTar(tar);
        const paths = parsed.map(([p]) => p);
        expect(paths).not.toContain('personalities/alice/mcp/linear/access_token');
        expect(paths).not.toContain('personalities/alice/mcp/linear/refresh_token');
        expect(paths).not.toContain('personalities/alice/mcp/linear/expires_at');
        expect(paths).toContain('personalities/alice/mcp/linear/config.yaml');
    });
});
