// Test group 3: secrets.manifest.yaml format
// ---------------------------------------------------------------------------
describe('secrets.manifest.yaml format', () => {
    it('backup manifest includes backed_up_at timestamp', () => {
        const entries = [
            { relPath: 'config.yaml', content: Buffer.from('provider: anthropic') },
            {
                relPath: 'secrets.manifest.yaml',
                content: Buffer.from('# Generated by ethos backup\nbacked_up_at: 2026-05-22T10:00:00Z\n\nglobal:\n  - key: anthropic-api-key\n    fill_with: ethos keys set anthropic-api-key <value>\n'),
            },
        ];
        const tar = buildTar(entries);
        const parsed = parseTar(tar);
        const manifest = parsed.find(([p]) => p === 'secrets.manifest.yaml');
        expect(manifest).toBeDefined();
        if (manifest) {
            const content = manifest[1].toString('utf8');
            expect(content).toContain('backed_up_at:');
            expect(content).toContain('global:');
        }
    });
    it('export manifest includes personality and exported_at', () => {
        const manifestContent = [
            '# Generated by ethos personality export',
            '# Fill these secrets before using this personality',
            'personality: alice',
            'exported_at: 2026-05-22T10:00:00Z',
            '',
            'mcp_auth:',
            '  - server: github',
            '    description: OAuth token for MCP server "github" (stripped from export)',
            '    fill_with: "ethos mcp auth github"',
            '',
        ].join('\n');
        const entries = [
            { relPath: 'personalities/alice/config.yaml', content: Buffer.from('name: alice') },
            { relPath: 'secrets.manifest.yaml', content: Buffer.from(manifestContent) },
        ];
        const tar = buildTar(entries);
        const parsed = parseTar(tar);
        const manifest = parsed.find(([p]) => p === 'secrets.manifest.yaml');
        expect(manifest).toBeDefined();
        if (manifest) {
            const content = manifest[1].toString('utf8');
            expect(content).toContain('personality: alice');
            expect(content).toContain('exported_at:');
            expect(content).toContain('mcp_auth:');
            expect(content).toContain('server: github');
        }
    });
});
