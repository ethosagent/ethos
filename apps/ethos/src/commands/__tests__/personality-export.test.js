// Test group 6: tar roundtrip with cron/jobs.json
// ---------------------------------------------------------------------------
describe('backup — cron/jobs.json inclusion', () => {
    it('cron/jobs.json roundtrips through tar correctly', () => {
        const cronContent = JSON.stringify([{ name: 'daily', schedule: '0 0 * * *' }]);
        const entries = [
            { relPath: 'config.yaml', content: Buffer.from('provider: anthropic') },
            { relPath: 'cron/jobs.json', content: Buffer.from(cronContent) },
        ];
        const tar = buildTar(entries);
        const parsed = parseTar(tar);
        expect(parsed).toHaveLength(2);
        const cronEntry = parsed.find(([p]) => p === 'cron/jobs.json');
        expect(cronEntry).toBeDefined();
        if (cronEntry) {
            expect(cronEntry[1].toString()).toBe(cronContent);
        }
    });
});
