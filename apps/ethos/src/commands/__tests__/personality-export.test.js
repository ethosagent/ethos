// Test group 5: Path traversal in personality export archives
// ---------------------------------------------------------------------------
describe('personality export — path traversal defense', () => {
    it('rejects archive entries with ../ path traversal', () => {
        const header = Buffer.alloc(512, 0);
        Buffer.from('personalities/../../../etc/passwd'.padEnd(100, '\0')).copy(header, 0);
        Buffer.from('0000644\0').copy(header, 100);
        Buffer.from('0000000\0').copy(header, 108);
        Buffer.from('0000000\0').copy(header, 116);
        const content = Buffer.from('malicious');
        const sizeOctal = content.length.toString(8).padStart(11, '0');
        Buffer.from(`${sizeOctal}\0`).copy(header, 124);
        Buffer.from('00000000000\0').copy(header, 136);
        header[156] = 0x30;
        Buffer.from('ustar\0').copy(header, 257);
        Buffer.from('00').copy(header, 263);
        let sum = 0;
        for (let i = 0; i < 512; i++)
            sum += header[i] ?? 0;
        Buffer.from(`${sum.toString(8).padStart(6, '0')}\0 `).copy(header, 148);
        const dataBlocks = Math.ceil(content.length / 512);
        const padded = Buffer.alloc(dataBlocks * 512, 0);
        content.copy(padded);
        const end = Buffer.alloc(1024, 0);
        const tar = Buffer.concat([header, padded, end]);
        expect(() => parseTar(tar)).toThrow('Malicious tar entry rejected');
    });
});
