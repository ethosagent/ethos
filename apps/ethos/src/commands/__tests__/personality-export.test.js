import { describe, expect, it } from 'vitest';
import { buildTar, parseTar } from '../backup';
// ---------------------------------------------------------------------------
// Test group 1: Backup archive content
// ---------------------------------------------------------------------------
describe('backup — archive content rules', () => {
    it('includes personality files in subdirectories (skills/)', () => {
        const entries = [
            { relPath: 'personalities/alice/config.yaml', content: Buffer.from('name: alice') },
            { relPath: 'personalities/alice/SOUL.md', content: Buffer.from('# Alice') },
            {
                relPath: 'personalities/alice/skills/code-review.md',
                content: Buffer.from('# Review skill'),
            },
            { relPath: 'personalities/alice/skills/nested/deep.md', content: Buffer.from('# Deep') },
        ];
        const tar = buildTar(entries);
        const parsed = parseTar(tar);
        expect(parsed).toHaveLength(4);
        const paths = parsed.map(([p]) => p);
        expect(paths).toContain('personalities/alice/skills/code-review.md');
        expect(paths).toContain('personalities/alice/skills/nested/deep.md');
    });
    it('does not include keys.json in entries', () => {
        // Verify that if someone accidentally added keys.json it would be present
        // but the backup code is supposed to NOT include it.
        // This is more of a documentation test — the real enforcement is in collectEntries.
        const entries = [
            { relPath: 'config.yaml', content: Buffer.from('provider: anthropic') },
            { relPath: 'MEMORY.md', content: Buffer.from('# Memory') },
        ];
        const tar = buildTar(entries);
        const parsed = parseTar(tar);
        const paths = parsed.map(([p]) => p);
        expect(paths).not.toContain('keys.json');
    });
});
